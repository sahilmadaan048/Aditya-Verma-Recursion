class Node{
    public:
    int data;
    Node* left;
    Node* right;

    Node(int data) {
        this->data = data;
        this->left = nullptr;
        this->right = nullptr;
    }
};


//it will height the height of the tree with root = root passeed 
int height(Node* root){
    if(root==nullptr){
        return 0;
    }
    int lh = height(root->left);
    int rh = height(root->right);
    return max(lh, rh)+1;
}


void solve(){
    Node * root = new Node(1);
    
    root->left = new Node(2);
    root->right = new Node(3);

    root->left->left = new Node(4);
    root->left->right = new Node(5);

    //height should be 3 for this tree
    cout<<height(root)<<endl;    
}

